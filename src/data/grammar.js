// Imperfect (लङ्): augment 'a' + present stem + laṅ endings
function mkImperfect(stemDev, stemIast, en3sg, enBase) {
  const s = stemIast.endsWith('a') ? stemIast.slice(0, -1) : stemIast
  return {
    p3sg: [`अ${stemDev}त्`,   `a${stemIast}t`,   `he/she ${enBase}`],
    p3du: [`अ${stemDev}ताम्`, `a${stemIast}tām`, `they two ${enBase}`],
    p3pl: [`अ${stemDev}न्`,   `a${stemIast}n`,   `they ${enBase}`],
    p2sg: [`अ${stemDev}ः`,    `a${stemIast}ḥ`,   `you ${enBase}`],
    p2du: [`अ${stemDev}तम्`,  `a${stemIast}tam`, `you two ${enBase}`],
    p2pl: [`अ${stemDev}त`,    `a${stemIast}ta`,  `you all ${enBase}`],
    p1sg: [`अ${stemDev}म्`,   `a${stemIast}m`,   `I ${enBase}`],
    p1du: [`अ${stemDev}ाव`,   `a${s}āva`,        `we two ${enBase}`],
    p1pl: [`अ${stemDev}ाम`,   `a${s}āma`,        `we ${enBase}`],
  }
}

// Future (लृट्): future stem (root/stem + iṣya) + present endings
function mkFuture(futDev, futIast, en3sg, enBase) {
  const b = futIast.endsWith('a') ? futIast.slice(0, -1) : futIast
  return {
    p3sg: [`${futDev}ति`,   `${futIast}ti`,   `he/she will ${enBase}`],
    p3du: [`${futDev}तः`,   `${futIast}taḥ`,  `they two will ${enBase}`],
    p3pl: [`${futDev}न्ति`, `${futIast}nti`,  `they will ${enBase}`],
    p2sg: [`${futDev}सि`,   `${futIast}si`,   `you will ${enBase}`],
    p2du: [`${futDev}थः`,   `${futIast}thaḥ`, `you two will ${enBase}`],
    p2pl: [`${futDev}थ`,    `${futIast}tha`,  `you all will ${enBase}`],
    p1sg: [`${futDev}ामि`,  `${b}āmi`,        `I will ${enBase}`],
    p1du: [`${futDev}ावः`,  `${b}āvaḥ`,       `we two will ${enBase}`],
    p1pl: [`${futDev}ामः`,  `${b}āmaḥ`,       `we will ${enBase}`],
  }
}

// Imperative (लोट्): present stem + loṭ endings
function mkImperative(stemDev, stemIast, enBase) {
  const s = stemIast.endsWith('a') ? stemIast.slice(0, -1) : stemIast
  return {
    p3sg: [`${stemDev}तु`,   `${stemIast}tu`,  `let him/her ${enBase}`],
    p3du: [`${stemDev}ताम्`, `${stemIast}tām`, `let them two ${enBase}`],
    p3pl: [`${stemDev}न्तु`, `${stemIast}ntu`, `let them ${enBase}`],
    p2sg: [stemDev,          `${s}a`,          `${enBase}! (you)`],
    p2du: [`${stemDev}तम्`,  `${stemIast}tam`, `${enBase}! (you two)`],
    p2pl: [`${stemDev}त`,    `${stemIast}ta`,  `${enBase}! (you all)`],
    p1sg: [`${stemDev}ानि`,  `${s}āni`,        `let me ${enBase}`],
    p1du: [`${stemDev}ाव`,   `${s}āva`,        `let us two ${enBase}`],
    p1pl: [`${stemDev}ाम`,   `${s}āma`,        `let us ${enBase}`],
  }
}

// Optative (विधिलिङ्): present stem + e-vowel + optative endings
function mkOptative(stemDev, stemIast, enBase) {
  const optDev  = stemDev + 'े'
  const optIast = (stemIast.endsWith('a') ? stemIast.slice(0, -1) : stemIast) + 'e'
  return {
    p3sg: [`${optDev}त्`,   `${optIast}t`,   `he/she should ${enBase}`],
    p3du: [`${optDev}ताम्`, `${optIast}tām`, `they two should ${enBase}`],
    p3pl: [`${optDev}युः`,  `${optIast}yuḥ`, `they should ${enBase}`],
    p2sg: [`${optDev}ः`,    `${optIast}ḥ`,   `you should ${enBase}`],
    p2du: [`${optDev}तम्`,  `${optIast}tam`, `you two should ${enBase}`],
    p2pl: [`${optDev}त`,    `${optIast}ta`,  `you all should ${enBase}`],
    p1sg: [`${optDev}यम्`,  `${optIast}yam`, `I should ${enBase}`],
    p1du: [`${optDev}व`,    `${optIast}va`,  `we two should ${enBase}`],
    p1pl: [`${optDev}म`,    `${optIast}ma`,  `we should ${enBase}`],
  }
}

function cl1(id, rootDev, rootIast, meaning, stemDev, stemIast, en3sg, enBase, classNum, note) {
  // For 1st-person endings, the stem-final 'a' merges with the ā of the ending.
  // In Devanagari use the dependent vowel sign ā (ा U+093E) so the matra attaches
  // to the final consonant of the stem rather than adding a separate आ character.
  // In IAST, strip the trailing 'a' from the stem before appending āmi/āvaḥ/āmaḥ.
  const stemBase = stemIast.endsWith('a') ? stemIast.slice(0, -1) : stemIast
  const forms = {
    p3sg: [`${stemDev}ति`,   `${stemIast}ti`,    `he/she ${en3sg}`],
    p3du: [`${stemDev}तः`,   `${stemIast}taḥ`,   `they two ${enBase}`],
    p3pl: [`${stemDev}न्ति`,  `${stemIast}nti`,   `they ${enBase}`],
    p2sg: [`${stemDev}सि`,   `${stemIast}si`,    `you ${enBase}`],
    p2du: [`${stemDev}थः`,   `${stemIast}thaḥ`,  `you two ${enBase}`],
    p2pl: [`${stemDev}थ`,    `${stemIast}tha`,   `you all ${enBase}`],
    p1sg: [`${stemDev}ामि`,  `${stemBase}āmi`,   `I ${enBase}`],
    p1du: [`${stemDev}ावः`,  `${stemBase}āvaḥ`,  `we two ${enBase}`],
    p1pl: [`${stemDev}ामः`,  `${stemBase}āmaḥ`,  `we ${enBase}`],
  }

  // Future stem: Class 10 uses present stem + iṣya; consonant roots use root + iṣya
  let futDev, futIast
  if (classNum === 10) {
    futDev  = `${stemDev}िष्य`
    futIast = `${stemBase}iṣya`
  } else if (rootDev.includes('्')) {
    futDev  = `${rootDev.replace('्', '')}िष्य`
    futIast = `${rootIast}iṣya`
  } else {
    // vowel-ending root — approximate with stem + sya
    futDev  = `${stemDev}स्य`
    futIast = `${stemBase}sya`
  }

  return {
    id,
    root:     `√${rootDev}`,
    rootIast: `√${rootIast}`,
    meaning,
    stem:     stemDev,
    stemIast,
    class:    classNum || 1,
    ...(note ? { note } : {}),
    forms,
    negForms: {
      p3sg: [`न ${forms.p3sg[0]}`, `na ${forms.p3sg[1]}`, `he/she does not ${enBase}`],
      p2sg: [`न ${forms.p2sg[0]}`, `na ${forms.p2sg[1]}`, `you do not ${enBase}`],
      p1sg: [`न ${forms.p1sg[0]}`, `na ${forms.p1sg[1]}`, `I do not ${enBase}`],
      p3pl: [`न ${forms.p3pl[0]}`, `na ${forms.p3pl[1]}`, `they do not ${enBase}`],
    },
    imperfect:  mkImperfect(stemDev, stemIast, en3sg, enBase),
    future:     mkFuture(futDev, futIast, en3sg, enBase),
    imperative: mkImperative(stemDev, stemIast, enBase),
    optative:   mkOptative(stemDev, stemIast, enBase),
  }
}

